let __locale = "ru";

function setLocale(l) {
  __locale = UI_I18N[l] ? l : "ru";
}

function tr(key, params) {
  let s = (UI_I18N[__locale] || UI_I18N.ru)[key];
  if (s == null) s = UI_I18N.ru[key] ?? key;
  if (params) { for (const [p, v] of Object.entries(params)) s = s.replaceAll("{" + p + "}", v); }
  return s;
}

// Pick the plural form index for `n` in the active locale. `forms` is the
// number of available forms: 2 => English rule (one/other), 3 => Slavic rule
// (one/few/many) used by ru/uk/pl.
function plural(n, forms) {
  const count = Array.isArray(forms) ? forms.length : Number(forms) || 2;
  const abs = Math.abs(Number(n) || 0);
  if (count < 3) return abs === 1 ? 0 : 1;
  const mod10 = abs % 10;
  const mod100 = abs % 100;
  if (mod10 === 1 && mod100 !== 11) return 0;
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return 1;
  return 2;
}

// Resolve a plural key whose value is "form1|form2|..." and return the form
// matching `n` under the active locale's rule. Falls back to the ru value, then
// to the key, like tr().
function trPlural(key, n) {
  let s = (UI_I18N[__locale] || UI_I18N.ru)[key];
  if (s == null) s = UI_I18N.ru[key] ?? key;
  const forms = String(s).split("|");
  return forms[plural(n, forms)] ?? forms[forms.length - 1];
}

function applyI18n() {
  const dict = UI_I18N[__locale] || UI_I18N.ru;
  for (const el of document.querySelectorAll("[data-i18n]")) {
    const k = el.getAttribute("data-i18n");
    if (dict[k] != null) el.textContent = dict[k];
  }
  for (const el of document.querySelectorAll("[data-i18n-ph]")) {
    const k = el.getAttribute("data-i18n-ph");
    if (dict[k] != null) el.setAttribute("placeholder", dict[k]);
  }
  for (const el of document.querySelectorAll("[data-i18n-title]")) {
    const k = el.getAttribute("data-i18n-title");
    if (dict[k] != null) el.setAttribute("title", dict[k]);
  }
  document.documentElement.lang = __locale;
}

