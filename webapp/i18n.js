let __locale = "ru";

function setLocale(l) {
  __locale = UI_I18N[l] ? l : "ru";
}

function tr(key, params) {
  let s = (UI_I18N[__locale] || UI_I18N.ru)[key];
  if (s == null) s = UI_I18N.ru[key] ?? key;
  if (params) { for (const [p, v] of Object.entries(params)) s = s.replaceAll("{" + p + "}", v); }
  return s;
}

function applyI18n() {
  const dict = UI_I18N[__locale] || UI_I18N.ru;
  for (const el of document.querySelectorAll("[data-i18n]")) {
    const k = el.getAttribute("data-i18n");
    if (dict[k] != null) el.textContent = dict[k];
  }
  for (const el of document.querySelectorAll("[data-i18n-ph]")) {
    const k = el.getAttribute("data-i18n-ph");
    if (dict[k] != null) el.setAttribute("placeholder", dict[k]);
  }
  for (const el of document.querySelectorAll("[data-i18n-title]")) {
    const k = el.getAttribute("data-i18n-title");
    if (dict[k] != null) el.setAttribute("title", dict[k]);
  }
  document.documentElement.lang = __locale;
}

